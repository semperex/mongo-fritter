package com.semperex.mongo_fritter.storage.manager;

public class DataSourceRegistrationStorageManagerException extends StorageManagerException {

    public DataSourceRegistrationStorageManagerException() {
    }

    public DataSourceRegistrationStorageManagerException(String message) {
        super(message);
    }

    public DataSourceRegistrationStorageManagerException(String message, Throwable cause) {
        super(message, cause);
    }

    public DataSourceRegistrationStorageManagerException(Throwable cause) {
        super(cause);
    }

    public DataSourceRegistrationStorageManagerException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
